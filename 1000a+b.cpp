/*Problem Description
Calculate A + B.


Input
Each line will contain two integers A and B.Process to end of file.


Output
For each case, output A + B in one line.


Sample Input
1 1


Sample Output
2*/
#include <conio.h>
#include <iostream>
using namespace std;

int main()
{
	int a, b;
	while (cin >> a >> b)
	{
		cout << a + b<<endl;
	}
	_getch();
	return 0;
}
